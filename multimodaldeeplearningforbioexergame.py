def sample_clip_from_video(video_path, start_frame, end_frame, n_frames=N_FRAMES):
    cap = cv2.VideoCapture(video_path)
    idxs = np.linspace(start_frame, end_frame - 1, n_frames, dtype=int)

    frames = []
    for idx in idxs:
        cap.set(cv2.CAP_PROP_POS_FRAMES, idx)
        ok, frame = cap.read()
        if not ok:
            frame = np.zeros((*IMG_SIZE, 3), dtype=np.uint8)
        else:
            frame = cv2.resize(frame, IMG_SIZE[::-1])
            frame = frame[..., ::-1]  # BGR to RGB
        frames.append(frame / 255.0)

    cap.release()
    return np.stack(frames).astype(np.float32)  # (T, H, W, 3)

def extract_hr_seq(hr_df, hr_col, start_sec, end_sec, step=1.0):
    # sample HR at 1 Hz using interpolation
    t0, t1 = start_sec, end_sec
    times = np.arange(t0, t1, step)
    if len(times) == 0:
        times = np.array([t0])

    if 'time_sec' in hr_df.columns:
        t = hr_df['time_sec'].values
        v = hr_df[hr_col].values
    else:
        # assume 1Hz, index -> seconds
        t = np.arange(len(hr_df))
        v = hr_df[hr_col].values

    # clamp
    times_clamped = np.clip(times, t.min(), t.max())
    hr_values = np.interp(times_clamped, t, v).astype(np.float32)
    return hr_values  # shape (T_hr,)

from functools import lru_cache

@lru_cache(maxsize=None)
def cached_hr_df(video_stem):
    hr_path = Path(HR_DIR) / f"{video_stem}_hr.csv"
    df, hr_col = load_hr_csv(hr_path)
    return df, hr_col

def multimodal_generator(records):
    recs = records.copy()
    random.shuffle(recs)

    for r in recs:
        video_path  = r["video_path"]
        start_frame = r["start_frame"]
        end_frame   = r["end_frame"]
        start_sec   = r["start_sec"]
        end_sec     = r["end_sec"]

        # Video
        clip = sample_clip_from_video(video_path, start_frame, end_frame)

        # HR sequence
        stem = Path(video_path).stem
        hr_df, hr_col = cached_hr_df(stem)
        hr_seq = extract_hr_seq(hr_df, hr_col, start_sec, end_sec, step=1.0)
        # Pad/truncate HR to a fixed length, e.g., 4 samples
        HR_LEN = 4
        if len(hr_seq) < HR_LEN:
            pad = np.full(HR_LEN - len(hr_seq), hr_seq[-1], dtype=np.float32)
            hr_seq_fixed = np.concatenate([hr_seq, pad])
        else:
            hr_seq_fixed = hr_seq[:HR_LEN]
        hr_seq_fixed = hr_seq_fixed.astype(np.float32)

        # Targets
        hr_class   = np.int32(r["hr_class"])
        diff_class = np.int32(r["diff_class"])
        perf_reg   = np.float32(r["perf"])

        yield (clip, hr_seq_fixed), (hr_class, diff_class, perf_reg)

