def build_video_branch():
    # Frame encoder: small MobileNetV2 (or EfficientNetB0)
    base = tf.keras.applications.MobileNetV2(
        input_shape=(H, W, 3),
        include_top=False,
        weights="imagenet"
    )
    base.trainable = False  # fine-tune later if needed

    i = layers.Input((T, H, W, 3))
    # Apply base CNN per frame
    x = layers.TimeDistributed(base)(i)
    x = layers.TimeDistributed(layers.GlobalAveragePooling2D())(x)  # (B, T, D)

    # Temporal Conv1D
    x = layers.Conv1D(64, kernel_size=3, padding="same", activation="relu")(x)
    x = layers.Conv1D(64, kernel_size=3, padding="same", activation="relu")(x)
    x = layers.GlobalAveragePooling1D()(x)  # (B, 64)

    x = layers.Dense(64, activation="relu")(x)
    return models.Model(i, x, name="video_branch")

def build_hr_branch():
    i = layers.Input((HR_LEN,))
    x = layers.Reshape((HR_LEN, 1))(i)
    x = layers.Conv1D(16, kernel_size=2, activation="relu", padding="same")(x)
    x = layers.Conv1D(32, kernel_size=2, activation="relu", padding="same")(x)
    x = layers.GlobalAveragePooling1D()(x)
    x = layers.Dense(32, activation="relu")(x)
    return models.Model(i, x, name="hr_branch")

def build_multimodal_model():
    video_in = layers.Input((T, H, W, 3), name="video_in")
    hr_in    = layers.Input((HR_LEN,),    name="hr_in")

    video_branch = build_video_branch()
    hr_branch    = build_hr_branch()

    z_v  = video_branch(video_in)
    z_hr = hr_branch(hr_in)

    # Gated fusion
    concat = layers.Concatenate()([z_v, z_hr])
    g = layers.Dense(64, activation="sigmoid")(concat)
    # ensure g has same dim as z_v / z_hr; adjust to 64
    z_v_proj  = layers.Dense(64)(z_v)
    z_hr_proj = layers.Dense(64)(z_hr)

    fused = g * z_v_proj + (1.0 - g) * z_hr_proj
    fused = layers.Dense(64, activation="relu")(fused)

    # Heads
    hr_out = layers.Dense(NUM_HR_CLASSES, activation="softmax", name="hr_intensity")(fused)
    diff_out = layers.Dense(NUM_DIFF_CLASSES, activation="softmax", name="diff_proxy")(fused)
    perf_out = layers.Dense(1, activation="linear", name="perf_reg")(fused)

    model = models.Model(
        inputs=[video_in, hr_in],
        outputs=[hr_out, diff_out, perf_out],
        name="bio_exergame_multimodal"
    )

    losses_dict = {
        "hr_intensity": losses.SparseCategoricalCrossentropy(),
        "diff_proxy":   losses.SparseCategoricalCrossentropy(),
        "perf_reg":     losses.MeanSquaredError()
    }

    loss_weights = {
        "hr_intensity": 1.0,
        "diff_proxy":   1.0,
        "perf_reg":     0.5
    }

    model.compile(
        optimizer=optimizers.Adam(1e-4),
        loss=losses_dict,
        loss_weights=loss_weights,
        metrics={
            "hr_intensity": "accuracy",
            "diff_proxy":   "accuracy",
            "perf_reg":     tf.keras.metrics.MeanAbsoluteError()
        }
    )

    return model

model = build_multimodal_model()
