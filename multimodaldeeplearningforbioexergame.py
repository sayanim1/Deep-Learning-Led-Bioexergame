def get_video_info(video_path):
    cap = cv2.VideoCapture(video_path)
    fps = cap.get(cv2.CAP_PROP_FPS)
    frames = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))
    cap.release()
    return {"fps": fps, "frames": frames}

def load_hr_csv(hr_path):
    df = pd.read_csv(hr_path)
    # find HR column (same logic as before)
    if 'HR' in df.columns:
        hr_col = 'HR'
    else:
        num_cols = df.select_dtypes(include='number').columns
        if len(num_cols) == 0:
            raise ValueError(f"No numeric columns found in {hr_path}")
        if 'time_sec' in num_cols and len(num_cols) > 1:
            hr_col = [c for c in num_cols if c != 'time_sec'][0]
        else:
            hr_col = num_cols[0]
    return df, hr_col

def interval_mean_hr(hr_df, hr_col, start_sec, end_sec):
    # identical to your previous function
    if 'time_sec' in hr_df.columns:
        window = hr_df[(hr_df['time_sec'] >= start_sec) &
                       (hr_df['time_sec'] < end_sec)]
        if len(window) > 0:
            return float(window[hr_col].mean())
        t = hr_df['time_sec'].values
        v = hr_df[hr_col].values
        q_times = np.array([
            max(min(start_sec, t[-1]), t[0]),
            max(min(end_sec, t[-1]), t[0])
        ])
        iq = np.interp(q_times, t, v)
        return float(iq.mean())
    else:
        start_idx = int(math.floor(start_sec))
        end_idx   = int(math.ceil(end_sec))
        start_idx = max(start_idx, 0)
        end_idx   = min(end_idx, len(hr_df))
        if start_idx >= end_idx:
            return float(hr_df.iloc[-1][hr_col])
        return float(hr_df.iloc[start_idx:end_idx][hr_col].mean())

def hr_to_class(hr_value):
    return int(np.digitize(hr_value, HR_BINS) - 1)

def load_perf_csv(perf_path):
    """
    perf_path CSV has columns:
    'interval' like '0-4s', '4-8s', ...
    'LE','IE','LH','IH','LE_Error','IE_Error','LH_Error','IH_Error'
    """
    df = pd.read_csv(perf_path)
    # parse start_sec and end_sec from 'interval' column
    starts = []
    ends   = []
    for s in df['interval']:
        # assumes '0-4s' pattern
        part = s.replace('s', '')
        a, b = part.split('-')
        starts.append(float(a))
        ends.append(float(b))
    df['start_sec'] = starts
    df['end_sec']   = ends
    return df

