import collections

def add_difficulty_labels(records):
    # Group by participant
    by_p = collections.defaultdict(list)
    for r in records:
        by_p[r["participant"]].append(r)

    # For each participant, compute normalized HR and performance
    for p, recs in by_p.items():
        hr_vals   = np.array([r["hr_mean"] for r in recs])
        perf_vals = np.array([r["perf"]    for r in recs])

        # simple z-score normalization
        hr_mean_p, hr_std_p   = hr_vals.mean(), max(hr_vals.std(), 1e-6)
        perf_mean_p, perf_std_p = perf_vals.mean(), max(perf_vals.std(), 1e-6)

        for r in recs:
            r["hr_norm"]   = (r["hr_mean"] - hr_mean_p) / hr_std_p
            r["perf_norm"] = (r["perf"]    - perf_mean_p) / perf_std_p

    # Now assign difficulty/load proxy classes via heuristic thresholds
    def assign_diff_class(perf_norm, hr_norm):
        # Example rules; tune thresholds empirically
        # Low difficulty: very good perf, low HR
        if perf_norm >  0.5 and hr_norm <  0.0:
            return 0  # low
        # High difficulty: low perf, high HR
        if perf_norm < -0.5 and hr_norm >  0.0:
            return 2  # high
        # Otherwise medium
        return 1

    for r in records:
        r["diff_class"] = assign_diff_class(r["perf_norm"], r["hr_norm"])

    return records

