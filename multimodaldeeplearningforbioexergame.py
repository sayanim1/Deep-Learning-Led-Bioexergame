def build_window_index(video_dir, hr_dir, perf_dir):
    records = []

    for v in Path(video_dir).glob("*.mp4"):
        video_path = str(v)
        stem = v.stem        # e.g. 'p1_ie'
        parts = stem.split("_")
        participant = parts[0]   # 'p1'
        cond       = parts[1]    # 'ie', 'lh', etc.

        hr_path   = Path(hr_dir)   / f"{stem}_hr.csv"         # p1_ie_hr.csv
        perf_path = Path(perf_dir) / f"{participant}_accuracy.csv"  # p1_accuracy.csv

        if not hr_path.exists() or not perf_path.exists():
            # print for debugging if needed
            # print("Skip", stem, "hr_exists:", hr_path.exists(), "perf_exists:", perf_path.exists())
            continue

        info = get_video_info(video_path)
        fps  = info["fps"]
        total_frames = info["frames"]
        total_sec = total_frames / fps

        hr_df, hr_col   = load_hr_csv(hr_path)
        perf_df         = load_perf_csv(perf_path)

        # Which accuracy column to use for this video
        acc_col = LABEL_MAP.get(cond.lower(), None)
        if acc_col is None or acc_col not in perf_df.columns:
            # print("No matching accuracy column for", stem)
            continue

        for _, row in perf_df.iterrows():
            start_sec = float(row['start_sec'])
            end_sec   = float(row['end_sec'])

            if end_sec > total_sec:
                continue

            start_f = int(round(start_sec * fps))
            end_f   = int(round(end_sec   * fps))

            # HR intensity label
            hr_mean  = interval_mean_hr(hr_df, hr_col, start_sec, end_sec)
            hr_class = hr_to_class(hr_mean)

            # Performance for this video type
            perf = float(row[acc_col])

            records.append({
                "video_path": video_path,
                "start_frame": start_f,
                "end_frame": end_f,
                "start_sec": start_sec,
                "end_sec": end_sec,
                "hr_mean": hr_mean,
                "hr_class": hr_class,
                "perf": perf,
                "participant": participant,
                "cond": cond,
            })

    return records

