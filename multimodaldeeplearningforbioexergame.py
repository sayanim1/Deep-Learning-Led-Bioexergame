def map_to_named_outputs(inputs, targets):
    # inputs:  (video, hr_seq)
    # targets: (hr_class, diff_class, perf_reg)
    video, hr_seq = inputs
    hr_class, diff_class, perf_reg = targets

    x = {
        "video_in": video,
        "hr_in":    hr_seq
    }
    y = {
        "hr_intensity": hr_class,
        "diff_proxy":   diff_class,
        "perf_reg":     tf.expand_dims(perf_reg, -1)
    }
    return x, y

